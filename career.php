            grasping a
            cell phone. Some time later, he was contacted by Manga Zoo (today a smartphone app), which offered to work
            with him,
            giving him a grant to turn his idea into an anime they could sell. In May 2001, he quit his job at Falcom
            and began to
            work on Voices. In an interview, Shinkai noted that production took around seven months of "real work".

